/*
1.  Sum of Two Numbers 
Write a program that stores the integers 50 and 100 in variables, and stores the sum of 
these two in a variable named  
total.
*/

#include <iostream>
using namespace std;

void main() {
	int fifty = 50;
	int oneHundred = 100;
	int total = fifty + oneHundred;

	cout << fifty << endl;
	cout << oneHundred << endl;
	cout << total << endl;

	return;
}